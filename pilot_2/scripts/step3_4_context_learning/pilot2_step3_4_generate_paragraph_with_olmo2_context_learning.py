def prepare_inference():
    input_dir = Path(INPUT_DIR)
    output_dir = Path(OUTPUT_DIR)

    ensure_dir(str(output_dir))

    input_files = sorted(input_dir.glob("*_relations.json"))
    if not input_files:
        raise FileNotFoundError(f"No *_relations.json files found in: {input_dir}")

    print("=" * 80)
    print("Preparing inference...")
    print(f"MODEL_NAME   = {MODEL_NAME}")
    print(f"INPUT_DIR    = {input_dir}")
    print(f"OUTPUT_DIR   = {output_dir}")
    print(f"HF_CACHE_DIR = {HF_CACHE_DIR}")
    print(f"USE_4BIT     = {USE_4BIT}")
    print(f"NUM_FILES    = {len(input_files)}")
    print("=" * 80)

    tokenizer, model = load_model_and_tokenizer(MODEL_NAME, USE_4BIT)

    print("=" * 80)
    print("Model loaded successfully.")
    print("=" * 80)

    return tokenizer, model, input_files, output_dir


def run_inference(input_files, output_dir, tokenizer, model):
    print("=" * 80)
    print(f"Running inference on {len(input_files)} files...")
    print("=" * 80)

    for input_path in input_files:
        process_file(
            input_path=input_path,
            output_dir=output_dir,
            tokenizer=tokenizer,
            model=model,
        )

    print("=" * 80)
    print("All files processed.")
    print("=" * 80)

tokenizer, model, input_files, output_dir = prepare_inference()

run_inference(input_files, output_dir, tokenizer, model)

