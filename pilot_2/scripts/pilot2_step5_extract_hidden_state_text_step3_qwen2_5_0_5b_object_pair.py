# Helpers
def ensure_dir(path: str) -> None:
    os.makedirs(path, exist_ok=True)


def load_json(path: str) -> Dict[str, Any]:
    with open(path, "r", encoding="utf-8") as f:
        return json.load(f)


def normalize_text(text: str) -> str:
    text = text.replace("\r\n", "\n").replace("\r", "\n")
    text = re.sub(r"[ \t]+", " ", text)
    text = re.sub(r"\n{2,}", "\n", text)
    return text.strip()


def is_step4_record(data: Dict[str, Any]) -> bool:
    return "rewritten_paragraph" in data or "rewritten_paragraphs" in data


def unify_record(data: Dict[str, Any], fallback_name: str) -> Dict[str, Any]:
    scene = data.get("scene", fallback_name)

    if is_step4_record(data):
        entity_alias_map = data.get("input_entity_alias_map", {}) or {}
        text_records = data.get("input_text_records", []) or []
        source_type = "step4"
    else:
        entity_alias_map = data.get("entity_alias_map", {}) or {}
        text_records = data.get("text_records", []) or []
        source_type = "step3"

    return {
        "scene": scene,
        "source_type": source_type,
        "entity_alias_map": entity_alias_map,
        "text_records": text_records,
    }


def mean_span(hidden_2d: torch.Tensor, start_idx: int, end_idx: int) -> torch.Tensor:
    span = hidden_2d[start_idx:end_idx]
    if span.numel() == 0:
        raise ValueError(f"Empty token span: {start_idx}-{end_idx}")
    return span.mean(dim=0)


def build_relation_examples(text_records: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    examples = []
    for rec in text_records:
        subj = rec.get("subject_alias")
        obj = rec.get("object_alias")
        rel = rec.get("relation")
        sent = rec.get("sentence")

        if subj and obj and rel and sent:
            examples.append({
                "subject_alias": subj,
                "object_alias": obj,
                "relation": rel,
                "sentence": normalize_text(sent),
                "relation_family": rec.get("relation_family"),
                "subject_id": rec.get("subject_id"),
                "object_id": rec.get("object_id"),
                "relation_index": rec.get("relation_index"),
            })
    return examples


def find_alias_token_spans(text: str, tokenizer, alias_list: List[str], max_length: int = 128):
    enc = tokenizer(
        text,
        return_tensors="pt",
        truncation=True,
        max_length=max_length,
        return_offsets_mapping=True,
        add_special_tokens=True,
    )

    input_ids = enc["input_ids"][0]
    attention_mask = enc["attention_mask"][0]
    offsets = enc["offset_mapping"][0].tolist()

    alias_to_char_span = {}

    for alias in alias_list:
        match = re.search(rf"(?<![A-Za-z0-9_]){re.escape(alias)}(?![A-Za-z0-9_])", text)
        if match is not None:
            alias_to_char_span[alias] = (match.start(), match.end())

    alias_to_token_span = {}

    for alias, (char_start, char_end) in alias_to_char_span.items():
        token_indices = []
        for tok_idx, (tok_start, tok_end) in enumerate(offsets):
            if tok_start == tok_end == 0:
                continue
            overlap = min(tok_end, char_end) - max(tok_start, char_start)
            if overlap > 0:
                token_indices.append(tok_idx)

        if token_indices:
            alias_to_token_span[alias] = (token_indices[0], token_indices[-1] + 1)

    return {
        "input_ids": input_ids,
        "attention_mask": attention_mask,
        "offset_mapping": offsets,
        "alias_to_token_span": alias_to_token_span,
    }


def load_model_and_tokenizer():
    tokenizer = AutoTokenizer.from_pretrained(
        MODEL_NAME,
        cache_dir=HF_CACHE_DIR,
        trust_remote_code=True,
    )

    if tokenizer.pad_token is None:
        tokenizer.pad_token = tokenizer.eos_token

    torch_dtype = torch.bfloat16 if torch.cuda.is_available() else torch.float32

    model = AutoModelForCausalLM.from_pretrained(
        MODEL_NAME,
        cache_dir=HF_CACHE_DIR,
        trust_remote_code=True,
        torch_dtype=torch_dtype,
    )
    model.to(DEVICE)
    model.eval()

    return tokenizer, model

# Sentence-level processing
@torch.no_grad()
def process_relation_example(ex: Dict[str, Any], tokenizer, model):
    sentence = ex["sentence"]
    subj = ex["subject_alias"]
    obj = ex["object_alias"]

    alias_list = [subj, obj]

    span_result = find_alias_token_spans(
        text=sentence,
        tokenizer=tokenizer,
        alias_list=alias_list,
        max_length=MAX_LENGTH,
    )

    alias_to_token_span = span_result["alias_to_token_span"]

    # Both must be found in the sentence.
    if subj not in alias_to_token_span or obj not in alias_to_token_span:
        return None

    input_ids = span_result["input_ids"].unsqueeze(0).to(DEVICE)
    attention_mask = span_result["attention_mask"].unsqueeze(0).to(DEVICE)

    outputs = model(
        input_ids=input_ids,
        attention_mask=attention_mask,
        output_hidden_states=True,
        return_dict=True,
        use_cache=False,
    )

    hidden_states = outputs.hidden_states

    layer_subject_vectors = []
    layer_object_vectors = []
    layer_diff_features = []
    layer_concat_features = []

    for layer_h in hidden_states:
        token_h = layer_h.squeeze(0).detach().cpu()  # [T, H]

        subj_start, subj_end = alias_to_token_span[subj]
        obj_start, obj_end = alias_to_token_span[obj]

        h_subj = mean_span(token_h, subj_start, subj_end)
        h_obj = mean_span(token_h, obj_start, obj_end)

        diff = h_subj - h_obj
        concat = torch.cat([h_subj, h_obj], dim=0)

        layer_subject_vectors.append(h_subj)
        layer_object_vectors.append(h_obj)
        layer_diff_features.append(diff)
        layer_concat_features.append(concat)

    return {
        "relation_index": ex.get("relation_index"),
        "sentence": sentence,
        "relation": ex["relation"],
        "relation_family": ex.get("relation_family"),
        "subject_alias": subj,
        "object_alias": obj,
        "subject_id": ex.get("subject_id"),
        "object_id": ex.get("object_id"),
        "input_ids": span_result["input_ids"].cpu(),
        "attention_mask": span_result["attention_mask"].cpu(),
        "offset_mapping": span_result["offset_mapping"],
        "decoded_tokens": tokenizer.convert_ids_to_tokens(span_result["input_ids"].tolist()),
        "alias_to_token_span": alias_to_token_span,
        "layer_subject_vectors": torch.stack(layer_subject_vectors, dim=0),   # [L+1, H]
        "layer_object_vectors": torch.stack(layer_object_vectors, dim=0),     # [L+1, H]
        "layer_diff_features": torch.stack(layer_diff_features, dim=0),       # [L+1, H]
        "layer_concat_features": torch.stack(layer_concat_features, dim=0),   # [L+1, 2H]
    }

# Main
@torch.no_grad()
def process_file(input_path: Path, output_dir: Path, tokenizer, model) -> None:
    data = load_json(str(input_path))
    record = unify_record(data, input_path.stem)

    scene = record["scene"]
    text_records = record["text_records"]
    entity_alias_map = record["entity_alias_map"]

    out_path = output_dir / f"{scene}_object_pair_sentence.pt"
    if out_path.exists() and not OVERWRITE:
        print(f"[Skip] {out_path.name} already exists")
        return

    relation_examples = build_relation_examples(text_records)

    sentence_pair_records = []
    skipped = 0

    for ex in relation_examples:
        result = process_relation_example(ex, tokenizer, model)
        if result is None:
            skipped += 1
            continue
        sentence_pair_records.append(result)

    save_obj = {
        "scene": scene,
        "source_type": record["source_type"],
        "model_name": MODEL_NAME,
        "entity_alias_map": entity_alias_map,
        "num_input_relations": len(relation_examples),
        "num_kept_relations": len(sentence_pair_records),
        "num_skipped_relations": skipped,
        "sentence_pair_records": sentence_pair_records,
    }

    torch.save(save_obj, out_path)
    print(f"[Done] {scene} -> {out_path}")
    print(f"       input_relations={len(relation_examples)}, kept={len(sentence_pair_records)}, skipped={skipped}")


def main():
    ensure_dir(OUTPUT_DIR)

    input_dir = Path(INPUT_DIR)
    output_dir = Path(OUTPUT_DIR)

    input_files = sorted(input_dir.glob("*.json"))
    if not input_files:
        raise FileNotFoundError(f"No JSON files found in {INPUT_DIR}")

    print("=" * 80)
    print("Running sentence-level object-pair extraction")
    print(f"MODEL_NAME = {MODEL_NAME}")
    print(f"INPUT_DIR  = {INPUT_DIR}")
    print(f"OUTPUT_DIR = {OUTPUT_DIR}")
    print(f"DEVICE     = {DEVICE}")
    print(f"NUM_FILES  = {len(input_files)}")
    print("=" * 80)

    tokenizer, model = load_model_and_tokenizer()

    for input_path in input_files:
        process_file(input_path, output_dir, tokenizer, model)

    print("=" * 80)
    print("All sentence-level object-pair files done.")
    print("=" * 80)

if __name__ == "__main__":
    main()

